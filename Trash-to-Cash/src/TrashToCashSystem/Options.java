    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed

        connect = DatabaseConnection.connectionDB();
        
        
        try {
            if (JOptionPane.showConfirmDialog(frame, "Add Record?", "Trash to Cash", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION) {

                String itemname = tfItem.getText();
                double value = Double.parseDouble(tfValue.getText());
                
                String ifItemExists = "SELECT * FROM materials WHERE item LIKE '" + itemname + "'";
                pre = (PreparedStatement) connect.prepareStatement(ifItemExists);
                rs = pre.executeQuery();
                
                if (rs.next()){
                    JOptionPane.showMessageDialog(frame, "Item already exits.");
                    pre.close();
                    rs.close();
                } else{
                    pre.close();
                    rs.close();
                    
                    String addItem = "INSERT INTO materials VALUES (?, ?, ?, ?)";

                    pre = (PreparedStatement) connect.prepareStatement(addItem);
                    pre.setString(1, itemname);
                    pre.setString(2, String.format("%.2f", value));
                    pre.setString(3, cbCategory.getSelectedItem().toString());
                    pre.setString(4, "unarchived");
                    pre.execute();
                    display();
                    pre.close();
                    
                    count += 1;
                    
                    char ch = '_';
                    itemname = itemname.toLowerCase();
                    itemname = itemname.replace(' ', ch);
                    String addColumnItem = "ALTER TABLE items ADD COLUMN " + itemname + " int (11) DEFAULT 0";
                    pre = (PreparedStatement) connect.prepareStatement(addColumnItem);
                    pre.execute();
                    pre.close();
                    
                }
                
                tfItem.setText("");
                tfValue.setText("");
                cbCategory.setSelectedItem("");
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Options.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnAddItemActionPerformed

