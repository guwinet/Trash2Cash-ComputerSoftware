    public void display() {
        try {

            connect = DatabaseConnection.connectionDB();

            item = (DefaultTableModel) tblItem.getModel();
            sector = (DefaultTableModel) tblSector.getModel();
            category = (DefaultTableModel) tblCategory.getModel();
            item.setRowCount(0);
            sector.setRowCount(0);
            category.setRowCount(0);

            String forItem = "SELECT * FROM materials WHERE status = 'unarchived'";

            pre = (PreparedStatement) connect.prepareStatement(forItem);
            rs = pre.executeQuery();

            while (rs.next()) {
                item.addRow(new Object[]{
                    rs.getString("item"),
                    rs.getString("value"),
                    rs.getString("category")
                });
            }

            pre.close();
            rs.close();
            
            String forSector = "SELECT * FROM sector";

            pre = (PreparedStatement) connect.prepareStatement(forSector);
            rs = pre.executeQuery();

            while (rs.next()) {
                sector.addRow(new Object[]{
                    rs.getString("sector")
                });
            }

            pre.close();
            rs.close();
            
            String forCategory = "SELECT * FROM item_category WHERE status = 'enabled'";
            
            pre = (PreparedStatement) connect.prepareStatement(forCategory);
            rs = pre.executeQuery();

            while (rs.next()) {
                category.addRow(new Object[]{
                    rs.getString("category")
                });
            }

            pre.close();
            rs.close();

        } catch (SQLException ex) {
            Logger.getLogger(Options.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
    public void PopulateCategory(){
        try {
            String categories = "SELECT * FROM item_category";
            pre = (PreparedStatement) connect.prepareStatement(categories);
            rs = pre.executeQuery();
            
            while (rs.next()){
                cbCategory.addItem(rs.getString("category"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Options.class.getName()).log(Level.SEVERE, null, ex);
        }
        
}
