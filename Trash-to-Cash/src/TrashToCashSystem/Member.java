    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        if (tfName.getText().equals("") || cbSector.getSelectedItem().equals("----") || cbBarangay.getSelectedItem().equals("----")){

            JOptionPane.showMessageDialog(this, "Invalid input. Fill in all fields.");
        } else {
            
            connect = DatabaseConnection.connectionDB();
            model = (DefaultTableModel) tblTable.getModel();

            try {
                String barangay = cbBarangay.getSelectedItem().toString();
                String name = tfName.getText();
                String sector = cbSector.getSelectedItem().toString();
                String date = date();
                
                String addMember = "INSERT INTO members (name, barangay, sector, date) VALUES (?, ?, ?, ?)";
                
                pre = (PreparedStatement) connect.prepareStatement(addMember);
                pre.setString(1, name);
                pre.setString(2, barangay);
                pre.setString(3, sector);
                pre.setString(4, date);
                
                pre.execute();
                pre.close();
                
                String memberID = "SELECT * FROM members ORDER BY memberID DESC LIMIT 1";
                String id = "";
                try {
                    pre = (PreparedStatement) connect.prepareStatement(memberID);
                    rs = pre.executeQuery();
                
                    while (rs.next()){
                        id = rs.getString("memberID");
                    }
                    
                    JOptionPane.showMessageDialog(this, "Member Added. \nMember ID: " + id);
                    model.setRowCount(0);
                    display();
                
                    
                } catch (SQLException ex) {
                    Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
            
    }//GEN-LAST:event_btnAddActionPerformed

