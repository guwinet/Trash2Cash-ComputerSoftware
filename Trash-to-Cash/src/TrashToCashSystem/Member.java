    private void tfSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfSearchKeyReleased
        try {
            // TODO add your handling code here:
            String update = "SELECT * FROM members WHERE name LIKE '" + tfSearch.getText() + "%'";
            
            pre = (PreparedStatement) connect.prepareStatement(update);
            rs = pre.executeQuery();
            model = (DefaultTableModel) tblTable.getModel();
            model.setRowCount(0);
            
            while (rs.next()){
                model.addRow(new Object[]{
                    
                    rs.getString("memberID"),
                    rs.getString("name"),
                    rs.getString("barangay"),
                    rs.getString("sector"),
                    rs.getString("date")
                        
                });
                
            }
            
            pre.execute();
            pre.close();
            
            filter(tfSearch.getText());
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tfSearchKeyReleased

    private void tblTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTableMouseClicked
        // TODO add your handling code here:
        int row = tblTable.getSelectedRow();
        String IDnum = tblTable.getValueAt(row, 0).toString();
        String name = tblTable.getValueAt(row, 1).toString();
        String barangay = tblTable.getValueAt(row, 2).toString();
        String sector = tblTable.getValueAt(row, 3).toString();
        String date = tblTable.getValueAt(row, 4).toString();

        int indexBrgy = 0;
        for (int i = 0; i < cbBarangay.getItemCount(); i++) {
            if (barangay.equals(cbBarangay.getItemAt(i))) {
                indexBrgy = i;
            }
        }

        int indexSec = 0;
        for (int i = 0; i < cbSector.getItemCount(); i++) {
            if (sector.equals(cbSector.getItemAt(i))) {
                indexSec = i;
            }
        }

        tfName.setText(name);
        tfIDNum.setText(IDnum);
        cbBarangay.setSelectedIndex(indexBrgy);
        cbSector.setSelectedIndex(indexSec);
        tfDate.setText(date);
    }//GEN-LAST:event_tblTableMouseClicked

